'''
Dan rented 2 movies to watch last night. The first was 100 minutes long, 
the second was 110 minutes long. How many hours did it take for Dan to watch the 2 movies?
'''

movie1min = 100
movie2min = 110
minPerHr = 60

# conv min to hr
spentTime = float((movie1min + movie2min)/minPerHr)

print("Total Hrs spent to watch movies = ",spentTime)

